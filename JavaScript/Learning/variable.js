// What are variables?

// variable is a container like a box. It can store any type of data. It can store a number, a string, a boolean, an array, an object, a function, etc.

/* Rules for naming a variable:

 *** variable name should be camleCase

1. A variable name can contain letters, digits, underscores, and dollar signs.
2. A variable name must begin with a letter
3. A variable name can also begin with $ and _ (but we will not use it usually)
4. Variable names are case sensitive (y and Y are different variables)
5. Reserved words (like JavaScript keywords) cannot be used as variable names
6. Variable names should be meaningful
7. Variable names should not be too long
8. Variable names should not be too short
9. Variable names should not contain spaces
10. Variable names should not contain special characters like !, @, #, $, %, ^, &, *, (, ), -, +, =, {, }, [, ], |, :, ;, <, >, ?, /, and so on.


*/

